import numpy as np

#1D array
a = np.array([1,2,3])
print(a)
#2D array
a = np.array([[1,2],[3,3]])
print(a)

#3D array
a = np.array([[1,2],[3,3],[5,6]])
print(a)

#create a ray which specific datatype
a= np.array([1,2,3], dtype=complex)
print(a)

a= np.array([1,2,3], dtype=float)
print(a)

a= np.array([1,2,3], dtype=int)
print(a)